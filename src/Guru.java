public class Guru {
    int idg;
    int idf;
    String nip;
    String nama;

    public Guru(int idg, int idf, String nip, String nama) {
        this.idg = idg;
        this.idf = idf;
        this.nip = nip;
        this.nama = nama;
    }

    public int getIdg() {
        return idg;
    }

    public void setIdg(int idg) {
        this.idg = idg;
    }

    public int getIdf() {
        return idf;
    }

    public void setIdf(int idf) {
        this.idf = idf;
    }

    public String getNip() {
        return nip;
    }

    public void setNip(String nip) {
        this.nip = nip;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

  
    
    
}
